import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { execFileSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const manifestPath = join(root, 'workspace-manifest.json');
const outputPath = join(root, 'CURRENT_STATE.md');

function git(dir, args) {
  try {
    return execFileSync('git', ['-C', dir, ...args], {
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return null;
  }
}

function repoStatus(dir) {
  const branch = git(dir, ['branch', '--show-current']) ?? 'n/a';
  const dirty = git(dir, ['status', '--short']) ?? '';
  const dirtyCount = dirty ? dirty.split('\n').filter(Boolean).length : 0;
  const head = git(dir, ['rev-parse', '--short', 'HEAD']) ?? 'n/a';
  const lastCommit = git(dir, ['log', '-1', '--format=%s']) ?? 'no commits yet';
  return { head, branch, dirtyCount, lastCommit };
}

const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
const generated = [];
generated.push('<!-- BEGIN GENERATED -->');
generated.push(`Last refreshed: ${new Date().toISOString()}`);
generated.push('');
generated.push('## Updated Repositories');
generated.push('');

const rootRepoPath = process.env.CODEX_HOME
  ? join(process.env.CODEX_HOME, 'testgen-suite-root-repo')
  : join(process.env.HOME || '/home/max', '.codex', 'memories', 'testgen-suite-root-repo');
const rootStatus = repoStatus(rootRepoPath);
generated.push(`- root-meta: \`${rootStatus.branch}\` @ \`${rootStatus.head}\` (${rootStatus.dirtyCount} dirty), last commit: ${rootStatus.lastCommit}`);

for (const app of manifest.apps) {
  const abs = join(root, app.path);
  const status = repoStatus(abs);
  generated.push(`- ${app.name}: \`${status.branch}\` @ \`${status.head}\` (${status.dirtyCount} dirty), last commit: ${status.lastCommit}`);
}

generated.push('');
generated.push('This block is regenerated by `scripts/update-workspace-state.mjs`.');
generated.push('<!-- END GENERATED -->');

const current = readFileSync(outputPath, 'utf8');
const block = generated.join('\n');
const start = current.indexOf('<!-- BEGIN GENERATED -->');
const end = current.indexOf('<!-- END GENERATED -->');

let next;
if (start !== -1 && end !== -1 && end >= start) {
  next = current.slice(0, start) + block + current.slice(end + '<!-- END GENERATED -->'.length);
} else {
  next = current.trimEnd() + '\n\n' + block + '\n';
}

if (current !== next) {
  writeFileSync(outputPath, next);
}
